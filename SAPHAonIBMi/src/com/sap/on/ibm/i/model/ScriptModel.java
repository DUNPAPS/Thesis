package com.sap.on.ibm.i.model;

public class ScriptModel {

	private String sid;
	private String user;
	private String password;
	private String global_Kernel;
	private String nightlyMake;
	private String stopSAP;
	private String applyKernel;
	private String startSAP;
	private String runHATest;
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getGlobal_Kernel() {
		return global_Kernel;
	}
	public void setGlobal_Kernel(String global_Kernel) {
		this.global_Kernel = global_Kernel;
	}
	public String getNightlyMake() {
		return nightlyMake;
	}
	public void setNightlyMake(String nightlyMake) {
		this.nightlyMake = nightlyMake;
	}
	public String getStopSAP() {
		return stopSAP;
	}
	public void setStopSAP(String stopSAP) {
		this.stopSAP = stopSAP;
	}
	public String getApplyKernel() {
		return applyKernel;
	}
	public void setApplyKernel(String applyKernel) {
		this.applyKernel = applyKernel;
	}
	public String getStartSAP() {
		return startSAP;
	}
	public void setStartSAP(String startSAP) {
		this.startSAP = startSAP;
	}
	public String getRunHATest() {
		return runHATest;
	}
	public void setRunHATest(String runHATest) {
		this.runHATest = runHATest;
	}
	
	
}
